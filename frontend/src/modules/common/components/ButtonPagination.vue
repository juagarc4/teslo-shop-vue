<script setup lang="ts">
import ArrowLeftIcon from '@/icons/ArrowLeftIcon.vue';
import ArrowRightIcon from '@/icons/ArrowRightIcon.vue';
interface Props {
  page: number;
  hasMoreData: boolean;
}

defineProps<Props>();
</script>

<template>
  <div class="flex justify-center py-10 bg-gray-100 space-x-3">
    <button
      :disabled="page === 1"
      @click="$router.push({ query: { page: page - 1 } })"
      class="flex items-center space-x-1.5 rounded-lg px-4 py-1.5 font-bold text-white bg-blue-500 disabled:bg-gray-300 disabled:text-black hover:cursor-pointer disabled:cursor-not-allowed hover:bg-blue-600 transition-all"
    >
      <ArrowLeftIcon :color="page === 1 ? '#000000' : '#ffffff'" />
      <span>Previous</span>
    </button>
    <button
      :disabled="!hasMoreData"
      @click="$router.push({ query: { page: page + 1 } })"
      class="flex items-center space-x-1.5 rounded-lg px-4 py-1.5 font-bold text-white bg-blue-500 disabled:bg-gray-300 disabled:text-black hover:cursor-pointer disabled:cursor-not-allowed hover:bg-blue-600 transition-all"
    >
      <span>Next</span>
      <ArrowRightIcon :color="!hasMoreData ? '#000000' : '#ffffff'" />
    </button>
  </div>
</template>
